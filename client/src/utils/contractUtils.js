// Provider 및 Signer 설정
export const getProvider = () => {
  if (window.ethereum) {
    return new ethers.BrowserProvider(window.ethereum);
  } else {
    // MetaMask가 설치되지 않은 경우 Ganache 직접 연결 (읽기 전용)
    return new ethers.JsonRpcProvider(GANACHE_URL);
  }
};

export const getSigner = async () => {
  const provider = getProvider();
  const signer = await provider.getSigner();
  return signer;
};

// IdentityNFT 컨트랙트 인스턴스 가져오기
export const getIdentityNFTContract = async (signerOrProvider) => {
  const provider = signerOrProvider || getProvider();
  const contract = new ethers.Contract(IDENTITY_NFT_ADDRESS, IDENTITY_NFT_ABI, provider);
  return contract;
};

// EnhancedVote 컨트랙트 인스턴스 가져오기
export const getEnhancedVoteContract = async (signerOrProvider) => {
  const provider = signerOrProvider || getProvider();
  const contract = new ethers.Contract(ENHANCED_VOTE_ADDRESS, ENHANCED_VOTE_ABI, provider);
  return contract;
};

// 추가적으로 필요한 함수들 (예: 지갑 연결, 계정 가져오기 등)
export const connectWallet = async () => {
  if (window.ethereum) {
    try {
      const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
      console.log("Connected accounts:", accounts);
      return accounts[0];
    } catch (error) {
      console.error("Error connecting to MetaMask:", error);
      throw error;
    }
  } else {
    alert("MetaMask is not installed. Please install it to use this dApp.");
    return null;
  }
};

export const getConnectedAccountTokenId = async () => {
  if (window.ethereum) {
    const provider = getProvider();
    const signer = await provider.getSigner();
    const contract = await getIdentityNFTContract(signer);
    const address = await signer.getAddress();
    const tokenId = await contract.getTokenIdByAddress(address);
    return tokenId.toString();
  }
  return null;
};

export const getContractOwner = async () => {
  if (window.ethereum) {
    const provider = getProvider();
    const contract = await getIdentityNFTContract(provider);
    return contract.owner();
  }
  return null;
};
